<template>
  <div class="projectList__container">
    <h1>Projects</h1>
    <router-link :to="{ name: 'CreateProject' }">
      <v-btn color="success"><v-icon left>mdi-plus</v-icon> New project</v-btn>
    </router-link>
    <div
      class="projectList__item"
      v-for="project in projects"
      :key="project.id"
    >
      <v-row>
        <v-col cols="12" sm="4">
          <router-link
            :to="{
              name: 'Project',
              params: { idProject: project.id, project: project },
            }"
          >
            <v-card elevation="2" class="projectCard">
              <v-card-title>
                {{ project.name }}
              </v-card-title>
              <v-card-text>
                <div class="start">Start : {{ project.start_date }}</div>
                <div class="end" v-if="project.end_date">
                  End : {{ project.end_date }}
                </div>
                <div clas s="state">
                  {{ project.state }}
                </div>
              </v-card-text>
              
            </v-card>
          </router-link>
          <div class="modify-btn">
            <v-btn class="mr-6" color="warning" @click="$router.push({
                name: 'ModifyProject',
                params: {
                  idProject: project.id,
                  name: project.name,
                  description: project.description,
                  start_date: project.start,
                  end_date: project.end ? project.end : null,
                  git_repo: project.git,
                  state: project.state,
                  isEdit: true,
                },
              })">Modify Project</v-btn>
          </div>
        </v-col>
      </v-row>
    </div>
  </div>
</template>

<script>
import { serverurl, port } from "../../config/backend.config";
import axios from "axios";
export default {
  data() {
    return {
      projects: null,
    };
  },
  methods:{
    
  },
  mounted() {
    axios.get(`http://${serverurl}:${port}/project`, {}).then((res) => {
      this.projects = res.data.projectList;
    });
  },
};
</script>

